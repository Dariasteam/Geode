#ifndef AXON_H
#define AXON_H

class axon {
private:
  double value;
  double weight;
public:
  axon (double w) : weight (w) {}
  void set_value (double v) { value = v; }
  double get_value () const { return value * weight; }
};


#endif // AXON_H
